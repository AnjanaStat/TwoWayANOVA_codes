# by fun1 and fun2 we find crtical value of asymptotic Max-T test
fun1<-function(d1,d2,s1)
{
  #find the estimate of the diagonal matrix D
  D=matrix(c(d1,0,0,d2),nrow=2,byrow=TRUE)
  # find the estimate of the dispersion matrix 
  S=matrix(c(d1^2,-s1,-s1,d2^2),nrow=2,byrow=TRUE)
  P=solve(D)%*%S%*%solve(D)
  mu=c(0,0)
  #generate sample from mulivariate normal with mean vector mu and covariance matrix P
  g<-mvrnorm(1,mu,P)
  T1=g[1];T2=g[2]
  #find the min-T value
  A=min(T1,T2,na.rm = FALSE)
  return(A)
}
fun2<-function(d1,d2,s1)
{
  # find 1000 min-T statistic values
  x<-replicate(1000,fun1(d1,d2,s1))
  # arrange them in increasing order
  y<-sort(x,decreasing=FALSE)
  # find the critical value at level of significance 0.05
  c<-y[950]
  return(c)
}
fun3<-function(mu11,mu12,mu13,mu21,mu22,mu23,mu31,mu32,mu33,n11,n12,n13,n21,n22,n23,n31,n32,n33,v11,v12,v13,v21,v22,v23,v31,v32,v33)
{
  g11<-rnorm(n11,mu11,sqrt(v11));g12<-rnorm(n12,mu12,sqrt(v12));g13<-rnorm(n13,mu13,sqrt(v13))
  g21<-rnorm(n21,mu21,sqrt(v21));g22<-rnorm(n22,mu22,sqrt(v22));g23<-rnorm(n23,mu23,sqrt(v23))
  g31<-rnorm(n31,mu31,sqrt(v31));g32<-rnorm(n32,mu32,sqrt(v32));g33<-rnorm(n33,mu33,sqrt(v33))
  s11=var(g11);s12=var(g12);s13=var(g13);s21=var(g21);s22=var(g22);s23=var(g23);s31=var(g31)
  s32=var(g32);s33=var(g33)
  S1=(s11/n11+s12/n12+s13/n13)/9; S2=(s21/n21+s22/n22+s23/n23)/9;S3=(s31/n31+s32/n32+s33/n33)/9
  m=(mean(g11)+mean(g12)+mean(g13)+mean(g21)+mean(g22)+mean(g23)+mean(g31)+mean(g32)+mean(g33))/9
  Y1=(mean(g11)+mean(g21)+mean(g31))/3;Y2=(mean(g12)+mean(g22)+mean(g32))/3
  Y3=(mean(g13)+mean(g23)+mean(g33))/3
  #m1=m+Y1;m2=m+Y2;m3=m+Y3
  X1=(mean(g11)+mean(g12)+mean(g13))/3;X2=(mean(g21)+mean(g22)+mean(g23))/3
  X3=(mean(g31)+mean(g32)+mean(g33))/3
  v1<-sqrt(S1+S2);v2<-sqrt(S2+S3)
  T1=(X2-X1)/v1;T2=(X3-X2)/v2
  A=min(T1,T2,na.rm = FALSE)
  d1=(sqrt(s21/n21+s22/n22+s23/n23+s11/n11+s12/n12+s13/n13))/3
  d2=(sqrt(s31/n31+s32/n32+s33/n33+s21/n21+s22/n22+s23/n23))/3
  s1=(s21/n21+s22/n22+s23/n23)/9
  out=fun2(d1,d2,s1)
  # count if the statistic value is greater than the crtical value
  a=0
  if(A>out)
    a=a+1
  return(a)
}
